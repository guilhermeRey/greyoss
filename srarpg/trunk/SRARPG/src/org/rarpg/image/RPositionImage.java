package org.rarpg.image;

public class RPositionImage {
	float [] initialPoint;
	float [] finalPoint; 
	
	public RPositionImage(RImage image){
		this.initialPoint = new float[2]; 
		this.finalPoint = new float[2];
	}
	public RPositionImage(RImage image, float [] initialPoint, float [] finalPoint){
		this.initialPoint = initialPoint;
		this.finalPoint = finalPoint; 
	}
	public void setInicialPoint(float x, float y){
		this.initialPoint[0] = x; 
		this.initialPoint[1] = y; 
	}
	public void setFinalPoint(float x, float y){
		this.finalPoint[0] = x; 
		this.finalPoint[1] = y;
	}
}
