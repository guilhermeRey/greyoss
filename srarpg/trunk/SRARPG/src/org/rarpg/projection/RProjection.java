package org.rarpg.projection;

import java.util.List;

/***
 * Used for communication between the GUI and the processing project.
 * @author Guilherme Rey e Aline Miyazaki
 *
 */
public class RProjection {
	private List<RDisplayObj> toDisplay;
	
	public RProjection(List<RDisplayObj> objectsToDisplay)
	{
		this.setDisplayObjects(objectsToDisplay);
	}

	public void setDisplayObjects(List<RDisplayObj> toDisplay) {
		this.toDisplay = toDisplay;
	}

	public List<RDisplayObj> getDisplayObjects() {
		return toDisplay;
	}
}
