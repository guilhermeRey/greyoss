package org.rarpg.projection;

import java.util.Map;

import org.rarpg.image.RImage;
import org.rarpg.info.CharacterSheet;

/***
 * Represents a character sheet that will be displayed by Processing
 * @author Guilherme Rey e Aline Miyazaki
 *
 */
public class RCharSheetDisplay extends RDisplayObj {
	private CharacterSheet charSheet;
	private RImage		   avatar;

	public RCharSheetDisplay(CharacterSheet charSheet, RImage avatar, Map<String, Object> properties) {
		super(properties);
		this.charSheet = charSheet;
		this.avatar = avatar;
	}
	
	public CharacterSheet getCharSheet() {
		return this.charSheet;
	}
	
	public RImage getAvatar() {
		return this.avatar;
	}
}
