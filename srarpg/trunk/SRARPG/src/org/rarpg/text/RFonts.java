package org.rarpg.text;

import processing.core.PApplet;
import processing.core.PFont;

public class RFonts {
	
	public static PFont calibri(PApplet p)
	{
		return p.loadFont("Calibri-20.vlw");
	}
	
	public static PFont verdana(PApplet p)
	{
		return p.loadFont("Verdana");
	}
	
	public static PFont createNewFont(PApplet p, String name, Float size)
	{
		return p.createFont(name, size, true);
	}
}
