package org.rarpg.info;

public class Weapon {

	private String name; 
	private int nh; 
	private String damage;
	
	public Weapon(String name, int nh, String damage){
		this.name = name;
		this.nh = nh; 
		this.damage = damage; 
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setNh(int nh) {
		this.nh = nh;
	}
	public int getNh() {
		return nh;
	}
	public void setDamage(String damage) {
		this.damage = damage;
	}
	public String getDamage() {
		return damage;
	} 
}
